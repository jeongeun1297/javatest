package ch03;

public class AssignmentOperatorExample {

	public static void main(String[] args) {
		int result=0;
		result+=10; //10증가
		System.out.println("result:"+result);
		result-=5;
		System.out.println("result:"+result);
		result*=3;
		System.out.println("result:"+result);
		result/=5;
		System.out.println("result:"+result);
		result%=3;
		System.out.println("result:"+result);
		
		

	}

}
